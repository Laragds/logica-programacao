#FUA para ler dois inteiros (variáveis A e B) e efetuar as operações de adição, subtração, multiplicação
#  e divisão de A por B apresentando ao final os quatro resultados obtidos. 

num_int_A=int(input('Digite um número inteiro\n'))
num_int_B=int(input('Digite um número inteiro novamente\n'))
addition=num_int_A + num_int_B
subtraction=num_int_A - num_int_B
multiplication=num_int_A * num_int_B
print(f'O valor da adição é: {addition}\n o valor da subtração é: {subtraction}\n o valor da multiplicação é: {multiplication}')