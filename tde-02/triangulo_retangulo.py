#FUA para calcular a área de um triângulo, 
#exibindo o resultado. A base e a altura são dados que devem ser lidos como entrada. 
 


face_a=float(input('Digite o lado A do nosso triângulo\n'))
face_b=float(input('Digite o lado B do nosso triângulo\n'))
area=(face_a * face_b)/2
print(f'A área do nosso triângulo é de {area}cm')

